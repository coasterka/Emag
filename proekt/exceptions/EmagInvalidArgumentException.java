package proekt.exceptions;



public class EmagInvalidArgumentException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5469041353151903271L;
	
	public EmagInvalidArgumentException() {
		super();
	}
	
	public EmagInvalidArgumentException(String message) {
		super(message);
	}
	
	public EmagInvalidArgumentException(Throwable t) {
		super(t);
	}
	
	public EmagInvalidArgumentException(String message, Throwable t) {
		super(message, t);
	}
	
		
	
		
		

}
